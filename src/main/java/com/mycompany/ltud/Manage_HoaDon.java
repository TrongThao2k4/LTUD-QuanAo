    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tableHD.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hóa đơn để xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tableHD.getModel();
        String maHoaDon = (String) model.getValueAt(selectedRow, 0);

        int option = JOptionPane.showConfirmDialog(
            this,
            "Bạn có chắc chắn muốn xóa hóa đơn có mã: " + maHoaDon + " không?",
            "Xác nhận xóa",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        if (option != JOptionPane.YES_OPTION) {
            return;
        }

        try (Connection conn = DatabaseConnection.getConnection()) {
            conn.setAutoCommit(false); // Bắt đầu transaction

            // 1. Xóa chi tiết hóa đơn trước
            String deleteCTHD = "DELETE FROM chi_tiet_hoa_don WHERE MAHD = ?";
            try (PreparedStatement psCT = conn.prepareStatement(deleteCTHD)) {
                psCT.setString(1, maHoaDon);
                psCT.executeUpdate();
            }

            // 2. Xóa hóa đơn
            String deleteHD = "DELETE FROM hoa_don WHERE MAHD = ?";
            try (PreparedStatement psHD = conn.prepareStatement(deleteHD)) {
                psHD.setString(1, maHoaDon);
                int rows = psHD.executeUpdate();

                if (rows > 0) {
                    conn.commit(); // Commit nếu thành công
                    JOptionPane.showMessageDialog(this, "Đã xóa hóa đơn và chi tiết liên quan thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    loadHoaDonData();
                } else {
                    conn.rollback(); // Rollback nếu không xóa được
                    JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn để xóa.", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi SQL: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

