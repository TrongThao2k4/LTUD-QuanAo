    public void loadHoaDonData() {
        DefaultTableModel model = (DefaultTableModel) tableHD.getModel();
        
        
        model.setColumnIdentifiers(new String[]{"MAHD", "MAKH", "user_id", "TONGTIEN", "NGAYLAP", "NGAYCAPNHAT"});
        
        model.setRowCount(0);
        
        String sql = "SELECT MAHD, MAKH, user_id, TONGTIEN, NGAYLAP, NGAYCAPNHAT FROM hoa_don";
        
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            
            while (rs.next()) {
                Object[] row = {
                    rs.getString("MAHD"),
                    rs.getString("MAKH"),
                    rs.getString("user_id"),
                    rs.getBigDecimal("TONGTIEN"),
                    rs.getString("NGAYLAP"),
                    rs.getString("NGAYCAPNHAT")
                };
                
                model.addRow(row);
            }
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi khi tải dữ liệu hóa đơn: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }        
    }
