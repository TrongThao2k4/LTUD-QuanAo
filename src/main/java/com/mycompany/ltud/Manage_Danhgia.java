    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:                                        
    int selectedRow = TableDanhgia.getSelectedRow();
        if (selectedRow != -1) {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa đánh giá này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                DefaultTableModel model = (DefaultTableModel) TableDanhgia.getModel();
                String ma = model.getValueAt(selectedRow, 0).toString();
                try (Connection con = DatabaseConnection.getConnection()) {
                    PreparedStatement pst = con.prepareStatement("DELETE FROM danh_gia WHERE MADANHGIA = ?");
                    pst.setString(1, ma);
                    pst.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Lỗi khi xóa: " + e.getMessage());
                }
                loadDanhGiaData();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:                           
    String keyword = txtTim.getText().trim().toLowerCase();
    List<Object[]> filtered = new ArrayList<>();

    // Nếu ô tìm kiếm trống thì hiện lại toàn bộ dữ liệu
    if (keyword.isEmpty()) {
        updateTable(danhGiaData);
        return;
    }

    for (Object[] row : danhGiaData) {
        String maKH = row[1].toString().toLowerCase();
        String maSP = row[2].toString().toLowerCase();
        String diem = row[3].toString().toLowerCase();

        // Nếu keyword là số → tìm theo điểm
        if (keyword.matches("\\d+")) {
            if (diem.equals(keyword)) {
                filtered.add(row);
            }
        } else {
            // Ngược lại, tìm theo Mã KH hoặc Mã SP
            if (maKH.contains(keyword) || maSP.contains(keyword)) {
                filtered.add(row);
            }
        }
    }

    updateTable(filtered);
    }//GEN-LAST:event_btnTimActionPerformed

