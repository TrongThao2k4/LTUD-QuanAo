    void loadDanhGiaData() {
    DefaultTableModel model = (DefaultTableModel) TableDanhgia.getModel();
    model.setColumnIdentifiers(new String[]{"MADANHGIA", "MAKH", "MASP", "DIEM","NHANXET","NGAYDANHGIA"});
    model.setRowCount(0);
    danhGiaData.clear(); // Xóa dữ liệu cũ

    try (Connection con = DatabaseConnection.getConnection()) {
        String sql = "SELECT MADANHGIA, MAKH, MASP, DIEM, NHANXET, NGAYDANHGIA FROM danh_gia";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            Object[] row = {
                rs.getString("MADANHGIA"),
                rs.getString("MAKH"),
                rs.getString("MASP"),
                rs.getInt("DIEM"),
                rs.getString("NHANXET"),
                rs.getString("NGAYDANHGIA")
            };
            model.addRow(row);               
            danhGiaData.add(row); 
        } 
        TableDanhgia.removeColumn(TableDanhgia.getColumnModel().getColumn(5));
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage());
    }       
    }
    private void updateTable(List<Object[]> data) {
    DefaultTableModel model = (DefaultTableModel) TableDanhgia.getModel();
    model.setRowCount(0); // Xoá dữ liệu cũ
    for (Object[] row : data) {
        model.addRow(row); // Thêm dòng mới
    }
    }



    private void filterByRating() {
        String selected = cmbLoc.getSelectedItem().toString();
        java.util.List<Object[]> filtered = new java.util.ArrayList<>();

        for (Object[] row : danhGiaData) {
            int diem = (int) row[3];
            if (selected.equals("Tất cả") || selected.equals("Lọc theo điểm")) {
                filtered.add(row);
            } else if (Integer.toString(diem).equals(selected)) {
                filtered.add(row);
            }
        }
        updateTable(filtered);
    }

    private void searchDanhGia() {
        String keyword = txtTim.getText().toLowerCase();
        java.util.List<Object[]> filtered = new java.util.ArrayList<>();

        for (Object[] row : danhGiaData) {
            String maKH = row[1].toString().toLowerCase();
            String maSP = row[2].toString().toLowerCase();
            if (maKH.contains(keyword) || maSP.contains(keyword)) {
                filtered.add(row);
            }
        }
        updateTable(filtered);
    }

