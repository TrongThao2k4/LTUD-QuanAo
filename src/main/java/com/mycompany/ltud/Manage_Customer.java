    void loadKhachHangData() {
        DefaultTableModel model = (DefaultTableModel) tableCustomers.getModel();
        model.setColumnIdentifiers(new String[]{"Mã KH", "Họ Tên", "Email", "SĐT", "Địa Chỉ", "Ngày Tạo"});
        model.setRowCount(0); // Xóa dữ liệu cũ
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement("SELECT * FROM KHACH_HANG");
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                Object[] row = {
                    rs.getString("MAKH"),
                    rs.getString("HOTEN"),
                    rs.getString("EMAIL"),
                    rs.getString("SDT"),
                    rs.getString("DIACHI"),
                    rs.getString("NGAYTAO")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
