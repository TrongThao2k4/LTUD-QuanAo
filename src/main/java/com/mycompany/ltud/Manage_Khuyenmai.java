    void loadKMData() {
       DefaultTableModel model = (DefaultTableModel) TableKM.getModel();
       model.setColumnIdentifiers(new String[]{"MAKM", "TENKM", "PHANTRAMGIAM", "NGAYBATDAU","NGAYKETTHUC", "NGAYTAO", "NGAYSUA"});
       model.setRowCount(0); // Xóa dữ liệu cũ
//       tableUsers.getColumnModel().getColumn(0).setPreferredWidth(30);  // ID
//       tableUsers.getColumnModel().getColumn(1).setPreferredWidth(150); // Username
       
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement("SELECT * FROM khuyen_mai");
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                Object[] row = {
                    rs.getString("MAKM"),
                    rs.getString("TENKM"),
                    rs.getString("PHANTRAMGIAM"),
                    rs.getString("NGAYBATDAU"),
                    rs.getString("NGAYKETTHUC"),
                };
                model.addRow(row);               
               
            } 
            TableKM.removeColumn(TableKM.getColumnModel().getColumn(6));
            //tableUsers.setModel(model); // Gắn model mới cho bảng
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage());
        }       
    }

