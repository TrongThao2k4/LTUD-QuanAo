    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = TableKM.getSelectedRow();
        if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn người dùng để xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
        }
        int option = JOptionPane.showConfirmDialog(this,"Bạn có chắc chắn muốn xóa người dùng này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
        String userID = TableKM.getValueAt(selectedRow, 0).toString();
        try { Connection conn = DatabaseConnection.getConnection();
            String sql = " DELETE from khuyen_mai WHERE MAKM = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, userID);
            int rows = ps.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                loadKMData();// Load lại bảng
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi " + e.getMessage());
        }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

