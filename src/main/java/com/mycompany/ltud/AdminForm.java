    void loadUserData() {
       DefaultTableModel model = (DefaultTableModel) tableUsers.getModel();
       model.setColumnIdentifiers(new String[]{"ID", "Username", "Email", "Role","Status","Created_at","Password"});
       model.setRowCount(0); // Xóa dữ liệu cũ
//       tableUsers.getColumnModel().getColumn(0).setPreferredWidth(30);  // ID
//       tableUsers.getColumnModel().getColumn(1).setPreferredWidth(150); // Username
       
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement("SELECT * FROM Users");
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                Object[] row = {
                    rs.getString("user_id"),
                    rs.getString("username"),
                    rs.getString("email"),
                    rs.getString("role"),
                    rs.getString("status"),
                    rs.getString("created_at"),
                    rs.getString("password")
                };
                model.addRow(row);               
            } 
            tableUsers.removeColumn(tableUsers.getColumnModel().getColumn(6));
            //tableUsers.setModel(model); // Gắn model mới cho bảng
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage());
        }       
    }
    
