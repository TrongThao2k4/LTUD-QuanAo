         int selectedRow = tableUsers.getSelectedRow();
         if(selectedRow == -1){
             JOptionPane.showMessageDialog(this,"Vui long User de hieu chinh", "Thong bao",JOptionPane.WARNING_MESSAGE);
             return;
         }

         String userId = (String) tableUsers.getValueAt(selectedRow, 0);
         String username = (String)tableUsers.getValueAt(selectedRow, 1);
         String email = (String)tableUsers.getValueAt(selectedRow, 2);
         String role = (String)tableUsers.getValueAt(selectedRow, 3);
