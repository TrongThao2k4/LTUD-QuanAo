    private String generateNextUserId(Connection conn) throws SQLException {
        String sql = "SELECT MAX(SUBSTRING(user_id, 3)) AS max_id FROM Users";
        try (PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                String maxIdStr = rs.getString("max_id");
                int maxId = (maxIdStr != null) ? Integer.parseInt(maxIdStr) : 0;
                return String.format("US%03d", maxId + 1);
            }
        }
        return "us001";
    }

