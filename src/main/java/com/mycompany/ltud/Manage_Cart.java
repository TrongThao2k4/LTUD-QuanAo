    void loadGioHangData() {
        DefaultTableModel model = (DefaultTableModel) tableGioHang.getModel();
        model.setColumnIdentifiers(new String[]{"Mã Giỏ Hàng", "Mã KH", "Mã SP", "Tên SP", "Số Lượng", "Ngày Thêm", "Kích Thước", "Màu Sắc"});
        model.setRowCount(0);
        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(
                 "SELECT gh.MAGIOHANG, gh.MAKH, gh.MASP, sp.TENSP, gh.SOLUONG, gh.NGAYTHEM, ctsp.KICHTHUOC, ctsp.MAUSAC " +
                 "FROM GIO_HANG gh " +
                 "LEFT JOIN SAN_PHAM sp ON gh.MASP = sp.MASP " +
                 "LEFT JOIN CHI_TIET_SAN_PHAM ctsp ON sp.MASP = ctsp.MASP");
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                Object[] row = {
                    rs.getString("MAGIOHANG"),
                    rs.getString("MAKH"),
                    rs.getString("MASP"),
                    rs.getString("TENSP"),
                    rs.getInt("SOLUONG"),
                    rs.getString("NGAYTHEM"),
                    rs.getString("KICHTHUOC"),
                    rs.getString("MAUSAC")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
